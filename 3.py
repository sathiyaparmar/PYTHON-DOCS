# Arithmetic operators 

num1, num2 = 1, 5

# Addition of numbers
print(num1 + num2) # 6

# Subtraction of numbers
print(num1 - num2) # -4

# Multiplication of numbers
print(num1 * num2) # 5

# Divide of numbers
print(num1 / num2) # 0.2




# // Divide and get floor point (quotient)
print(num1 // num2) # = int(num1/num2) # 0
print(5 // 2) # 2

# % modulo (remainder)
print(num1 % num2) # 1
print(5 % 2) # 1

# Power
print(pow(num2,2)) 
print(num2 ** 2) # = # pow(number, power)
